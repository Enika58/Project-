#Напишите функцию, которая определяет - високосный это был год, или нет?
def определить_високосный_год(год):
    if год % 4 == 0 and (год % 100 != 0 or год % 400 == 0):
        return True
    else:
        return False

# Пример использования функции
год = int(input("Введите год: "))

if определить_високосный_год(год):
    print("Год", год, "является високосным.")
else:
    print("Год", год, "не является високосным.")

