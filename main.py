#Напишите функцию, которая определяет сколько сейчас лет пользователю;
def определить_возраст():
    год_рождения = int(input("Введите год рождения: "))
    сегодня = datetime.date.today()
    возраст = сегодня.year - год_рождения
    
    return возраст

# Пример использования функции
возраст = определить_возраст()
print("Вам сейчас", возраст, "лет.")

